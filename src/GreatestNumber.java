public class GreatestNumber {
    public static void main (String[] args)
    {
        int num1 = 45, num2 = 625;
        if (num1 == num2)
            System.out.println("both are equal");
        else if (num1 > num2)
            System.out.println(num1 + " num1 is greater");
        else
            System.out.println(num2 + " num2 is greater");
    }
}
